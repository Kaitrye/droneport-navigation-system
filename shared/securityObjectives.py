from enum import Enum


class SecurityObjective(Enum):
    """
    Цели безопасности (ЦБ) с описанием и весом.
    
    Вес отражает сложность и важность достижения ЦБ:
    - Чем выше вес, тем сложнее/дороже реализовать данную ЦБ
    - Дроны с ЦБ высокого веса получают большую скидку на страховку
    - Шкала весов: 1-5 (1 = базовый, 5 = критически важный)
    
    Значение: кортеж (описание, вес)
    """
    SO_1 = ("При любых обстоятельствах для взаимодействия с наземными службами используются только авторизованные бортовые радиостанции", 3)
    
    SO_2 = ("При любых обстоятельствах выполняются только авторизованные полётные задания", 4)
    
    SO_3 = ("При любых обстоятельствах в наземные службы передаётся только аутентичный идентификатор БВС", 3)
    
    SO_4 = ("При любых обстоятельствах только авторизованные пользователи имеют доступ к конфиденциальной информации", 4)
    
    SO_5 = ("При любых обстоятельствах выполняются только аутентичные управляющие команды оператора наземных управляющих станций / ОрВД", 5)
    
    SO_6 = ("При любых обстоятельствах выполняются полёты выполняются в соответствие с ограничениями в полётном задании и управляющими командами", 5)
    
    SO_7 = ("При любых обстоятельствах полёты выполняются только с включёнными проблесковыми маяками", 1)
    
    SO_8 = ("При любых обстоятельствах все критичные данные записываются в бортовой журнал событий", 2)
    
    SO_9 = ("При любых обстоятельствах во время выполнения полётного задания в бортовой журнал событий возможна только запись новых данных", 2)
    
    SO_10 = ("При любых обстоятельствах во время полёта БВС находится на безопасном расстоянии от других воздушных судов", 5)
    
    SO_11 = ("При любых обстоятельствах устанавливаются только аутентичные обновления", 4)

    @property
    def description(self) -> str:
        """Возвращает текстовое описание ЦБ"""
        return self.value[0]

    @property
    def weight(self) -> int:
        """Возвращает вес ЦБ"""
        return self.value[1]